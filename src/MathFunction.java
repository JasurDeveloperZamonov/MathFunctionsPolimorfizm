public class MathFunction {
    public double a;
    public double b;


    public double mathAdd(){
        return a+b;
    }
    public double mathSub(){
        return a-b;
    }
    public double mathMultiply(){
        return a*b;
    }
    public double mathDiv(){
        return a/b;
    }
    public double mathABS(){
        return Math.abs(a);
    }
    public double mathPow(){
        return Math.pow(a,2);
    }
}
